#include "so_long.h"

int	get_pos_j(char **str, char c)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		j = 0;
		while (str[i][j])
		{
			if (str[i][j] == c)
				return (i);
			j++;
		}
		i++;
	}
	return (-1);
}
