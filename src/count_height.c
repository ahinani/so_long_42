#include "so_long.h"

int	count_height(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] == '\n' && str[i - 1] != '1')
			break ;
		if (str[i] == '\n')
			j++;
		i++;
	}
	if (str[i] == '\0')
		j++;
	return (j);
}
